C     --- FOUR-CENTER ELECTRON REPULSION INTEGRAL ---
C     -----------------------------------------------------
C      WRITE(6,*) ' ONE CENTER NUC. ATTRACTION INTEGRAL = ',EN
C      WRITE(7,*) ' ONE CENTER NUC. ATTRACTION INTEGRAL = ',EN
C
	EN=TWO*OVERLAP*DSQRT(DETAB/(PI*(ALK2+BLK12)))

C	WRITE(6,*)'OV=',OVERLAP

C
C      EN=EN*XK1*XK2*ZZ
C      RRX=(ALK1*(ALK2+BLK12)*RX1+ALK2*BLK12*RX2)/DETAB 
C      RRY=(ALK1*(ALK2+BLK12)*RY1+ALK2*BLK12*RY2)/DETAB 
C
      RRZ=(ALK1*(ALK2+BLK12)*RZ1+ALK2*BLK12*RZ2)/DETAB 
C      RE2=(RRX-EX)**2+(RRY-EY)**2+(RRZ-EZ)**2
      RE2=(RRZ-EZ)**2

      DELTA2=(ALK2+BLK12)/(FOUR*DETAB)
      RE2=RE2/(FOUR*DELTA2)
C      WRITE(6,*)EN
C      WRITE(6,*)RE2
      EN=EN*F0(RE2) 
C     -----------------------------------------------------
C      GOTO 1500
C300   KLM=5
C
C
	IF (DFLAG .EQ. 1) THEN
C
	Z=RE2
	PHIF=((((((C6*Z + C7)*Z + C8)*Z+ C9)*Z + C10)*Z + C11)*Z + 1.00D+0
	-0)
	THETAF=(((((C1*Z + C2)*Z + C3)*Z + C4)*Z+ C5)*Z +1.00D+00)
C
	OKL=DSQRT(DETAB)
C
C	QKL=(RRX-EX)**2.00D+00+(RRY-EY)**2.00D+00+(RRZ-EZ)**2.00D+00
	QKL=(RRZ-EZ)**2.00D+00
C
C	DENDA1:
C	======================================================
	DDETAB=ALKXX
