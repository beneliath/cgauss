C	DTEMP=PI
C
C	PKL=DSQRT(1.00D+00/(PI*(ALK2+BLK12)))
C	DPkl=(-1.00D+00/(2.00D+00*((PI*ALK2+PI*BLK12)*DSQRT((PI*ALK2+PI*BL
C	-K12)))))*DTEMP
	DPkl=(-1.00D+00/(2.00D+00*((PI*ALK2+PI*BLK12)*DSQRT((PI*ALK2+PI*BL
	-K12)))))*PI
