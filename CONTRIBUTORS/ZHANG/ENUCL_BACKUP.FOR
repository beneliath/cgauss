C	WRITE(6,*)'RZ2=',RZ2

C      DD=(RX1-RX2)**2+(RY1-RY2)**2+(RZ1-RZ2)**2

C      ZZ=EXP(-(ALK1*ALK2*BLK12)*DD/DETAB)
