C	ANKL=OKL*PKL
C	AMKL=2.00D+00*OVERLAP*ANKL
	DOKL=(1.00D+00/(2.00D+00*DSQRT(DETAB)))*DDETAB
C
	DNkl=(OKL*DPKL + PKL*DOKL)
C
	DMKL=2.00D+00*(OVERLAP*DNKL + ANKL*DOVnDA5)
C
	DENDA5=AMKL*DF0 + FF*DMKL
C	WRITE(6,*)'MKL=',AMKL
C	WRITE(6,*)'DF0=',DF0
C	WRITE(6,*)'PHIF=',PHIF
