C	======================================================
C	DDETAB=0.00D+00
C
	DZZ=2.00D+00*A3*BLK12*(-0.70055D+00+RRZ)/ALKXX
C
C	DTHETAF=5.00D+00*C1*Z**4.00D+00*DZZ+4.00D+00*C2*Z**3.00D+00*DZZ+3.
