C	DPHIF=6.00D+00*C6*Z**5.00D+00*DZZ+5.00D+00*C7*Z**4.00D+00*DZZ+4.00
C	-D+00*C8*Z**3.00D+00*DZZ+3.00D+00*C9*Z**2.00D+00*DZZ+2.00D+00*C10*Z
