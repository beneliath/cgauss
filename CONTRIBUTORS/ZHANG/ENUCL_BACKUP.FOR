	DZZ=(DDETAB)*QKL/ALKXX-DETAB*QKL/ALKXX**2.00D+00+DETAB*(2.00D+00*
	-((A1*AZ+A2*BZ+A3*CZ+A4*DZ)/DETAB-(ALK1+ALK2)*(ALKXX*(A1*AZ+A2*BZ)
     -+BLK12*(A3*CZ+A4*DZ))/DETAB**2.00D+00)*(-0.70055D+00+RRZ))/ALKXX
C
C	DTHETAF=5.00D+00*C1*Z**4.00D+00*DZZ+4.00D+00*C2*Z**3.00D+00*DZZ+3.
C	-00D+00*C3*Z**2.00D+00*DZZ+2.00D+00*C4*Z*DZZ+C5*DZZ
	DTHETAF=(5.00D+00*C1*Z**4.00D+00+4.00D+00*C2*Z**3.00D+00+3.00D+00*
	-C3*Z**2.00D+00+2.00D+00*C4*Z+C5)*DZZ
