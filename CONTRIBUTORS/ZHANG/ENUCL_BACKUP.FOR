C
	DENDAZ=AMKL*DF0 + FF*DMKL
C	WRITE(6,*)'MKL=',AMKL
C	WRITE(6,*)'DF0=',DF0
C	WRITE(6,*)'PHIF=',PHIF
C	WRITE(6,*)'THETAF=',THETAF
