	COMMON /DOVn/ DOVnDA1,DOVnDA2,DOVnDA5,DOVnDAZ,DOVnDBZ
C      DATA ONE,TWO,FOUR,SIX,EIGHT,HALF/1.,2.,4.,6.,8.,.5/               
	PARAMETER(PI=3.1415926535898D+00)
C      PI52=PI**2.5D+00
	PARAMETER(RX1=0.)
	PARAMETER(RX2=0.)
	PARAMETER(RY1=0.)
	PARAMETER(RY2=0.)
	PARAMETER(RRX=0.)
	PARAMETER(RRY=0.)
C     -----------------------------------------------------
C     ...P.M.Kozlowski & L. Adamowicz J.C.P 95, 6681 (1991)    
C     -----------------------------------------------------
      ALK1=A1+A2                                                       
      ALK2=A3+A4                                                       
      BLK12=A5+A6  
      ALKXX=ALK2+BLK12
      DETAB=ALK1*ALK2+(ALK1+ALK2)*BLK12
      IF(ALKXX.LE.0.0D+00) GOTO 300
      IF(DETAB.LE.0.0D+00) GOTO 300                       
C     -----------------------------------------------------
C     --- ONE CENTER NUCLEAR INTEGRAL ---
C     -----------------------------------------------------
C      EN=TWO*PI52/(DETAB*DSQRT(ALK2+BLK12))
C     -----------------------------------------------------
C     --- CENTERS ---
C     -----------------------------------------------------
C      XX1=(AX-BX)**2+(AY-BY)**2+(AZ-BZ)**2
C      XK1=EXP(-((A1*A2)/ALK1)*XX1) 
C      XX2=(CX-DX)**2+(CY-DY)**2+(CZ-DZ)**2
C      XK2=EXP(-((A3*A4)/ALK2)*XX2) 
