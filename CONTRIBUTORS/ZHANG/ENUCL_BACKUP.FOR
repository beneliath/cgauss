C      RX1=(A1*AX+A2*BX)/ALK1
C      RX2=(A3*CX+A4*DX)/ALK2
C      RY1=(A1*AY+A2*BY)/ALK1
C      RY2=(A3*CY+A4*DY)/ALK2
C
      RZ1=(A1*AZ+A2*BZ)/ALK1
      RZ2=(A3*CZ+A4*DZ)/ALK2

