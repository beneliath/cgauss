	-0)*DPHIF+(1.00D+00/PHIF)*DTHETAF)
C
C	FF=DSQRT(THETAF/PHIF)
C
C	DTEMP=PI
C
C	PKL=DSQRT(1.00D+00/(PI*(ALK2+BLK12)))
C	DPkl=(-1.00D+00/(2.00D+00*((PI*ALK2+PI*BLK12)*DSQRT((PI*ALK2+PI*BL
C	-K12)))))*DTEMP
	DPkl=(-1.00D+00/(2.00D+00*((PI*ALK2+PI*BLK12)*DSQRT((PI*ALK2+PI*BL
	-K12)))))*PI
C
C	ANKL=OKL*PKL
C	AMKL=2.00D+00*OVERLAP*ANKL
	DOKL=(1.00D+00/(2.00D+00*DSQRT(DETAB)))*DDETAB
C
	DNkl=(OKL*DPKL + PKL*DOKL)
C
	DMKL=2.00D+00*(OVERLAP*DNKL + ANKL*DOVnDA2)
C
	DENDA2=AMKL*DF0 + FF*DMKL
C	WRITE(6,*)'MKL=',AMKL
C	WRITE(6,*)'DF0=',DF0
C	WRITE(6,*)'PHIF=',PHIF
C	WRITE(6,*)'THETAF=',THETAF
C	WRITE(6,*)'DPHIF=',DPHIF
C	WRITE(6,*)'DTHETAF=',DTHETAF
C	WRITE(6,*)'DZZ=',DZZ
C	WRITE(6,*)'FF=',FF
C	WRITE(6,*)'DMKL=',DMKL
C	WRITE(6,*)'OVERLAP=',OVERLAP
C	WRITE(6,*)'NKL=',ANKL
C	CALL BIOUT(' ')
C
C
C	DENDA5:
C	======================================================
	DDETAB=ALK1+ALK2
C
C	DZZ=1.00D+00*(ALK1+ALK2)*QKL/ALKXX-1.00D+00*DETAB*QKL/ALKXX**2.00D
C	-+00+1.00D+00*DETAB*(2.00D+00*((A1*AX+A2*BX+A3*CX+A4*DX)/DETAB-(ALK
